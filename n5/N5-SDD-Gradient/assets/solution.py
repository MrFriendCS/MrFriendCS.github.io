# Declare the Variables
rise = 0
run = 0
gradient = 0.0 

# Get the values from the user
rise = int(input("Rise: "))
run = int(input("Run: "))

# Calculate the answer
gradient = rise / run

# Display the answer
print ("Gradient: " + str(gradient))