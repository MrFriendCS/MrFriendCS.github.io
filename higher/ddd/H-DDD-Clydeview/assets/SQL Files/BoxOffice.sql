CREATE TABLE BoxOffice (
    movieID INT NOT NULL,
    rating FLOAT NOT NULL
        CHECK(rating >=0 AND
              rating <= 10),
    movieBudget INT NOT NULL
        CHECK(movieBudget >= 0),
    salesUS INT NOT NULL
        CHECK(salesUS >= 0),
    salesInternational INT NOT NULL
        CHECK(salesInternational >= 0),
    FOREIGN KEY (movieID)
        REFERENCES Movie(movieID),
    PRIMARY KEY (movieID)
);

